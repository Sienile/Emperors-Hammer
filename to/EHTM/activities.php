<h2>Activities</h2>

<p>The Emperor's Hammer is primarily based on online multiplayer or role play gaming in the Star Wars universe, however, there are many other activities and events available.</p>

<p>The primary activities center around online multiplaying for they set a theme for each Group. The TIE Corps focuses on the space combat sims, the Hammer's Fist on the First Person Shooters, the Directorate on Strategy and the Fringe on Star Wars Galaxies (Star Wars themed Massive Multiplayer Online Role Playing Game). The Dark Brotherhood of Jedi is the only exception as it focuses on many aspects and adds in the element of Force users.</p>

<p>The Emperor's Hammer has created thousands of missions/maps for Star Wars games. They are available for download from the <a href="http://www.emperorshammer.org/battlecenter.php" target="blank"><strong>Emperor's Hammer Battle Center</strong></a>.</p>

