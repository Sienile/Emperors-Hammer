<h4>Gaming Activities</h4>
<p><strong>Space Simulators</strong><br />
For those whose interested in vaping their enemies into tiny bits in fast space combat, the EH provides a solid membership where players for online multiplaying can be found, as well as many custom missions for single player in all of the supported games:</p>
<ul>
	<li> X-Wing (XW)</li>
	<li> TIE Fighter &amp; TIE Fighter CD (TF)</li>
	<li> X-wing vs Tie Fighter (XvT)</li>
	<li> Balance of Power (BoP)</li>
	<li> X-Wing Alliance(XWA)</li>
	<li>Freeworlds</li>
</ul>

<p><strong>First Person Shooters</strong><br />
For those who prefer a more personal approach and to "pound the ground", the EH supports all multiplayer Star Wars themed First Person Shooters, be it a trooper or Dark Jedi. The games currently supported are:</p>
<ul>
	<li>Call of Duty 4 with Star Wars Mod: Galactic Warfare</li> 
	<li>Battlefield 2142 with First Strike mod</li> 
	<li>Dark Forces 2: Jedi Knight</li>
	<li>Jedi Knight 2: Jedi Outcast</li>
	<li>Jedi Knight 3: Jedi Academy</li>
	<li>Republic Commando</li>
	<li>Star Wars: Battlefront series </li>
</ul>

<p><strong>Real Time Strategy Games</strong><br />
For those who prefer the careful planning of strategies and the decisive strike against your enemies. Real Time Strategy games are also supported in the Emperor's Hammer:</p>
<ul>
	<li>Star Wars Force Commander (limited support)</li>
	<li>Star Wars Galactic Battlegrounds + Clone Campaigns</li>
	<li>Star Wars Empire at War + Forces of Corruption</li>
	<li>Sins of a Galactic Empire</li>
</ul>

<p><strong>Role Playing</strong><br />
For those who prefer live action character development in a Star Wars based universe. Role playing games are also supported in the Emperor's Hammer:</p>
<ul>
	<li>Star Wars Galaxies</li>
	<li>Star Wars The Old Republic</li>
</ul>

<h4>Creative Activities</h4>
<p><strong>Fiction</strong><br />
For creative minds that enjoy stories, the Emperor's Hammer allows members to participate in a great deal of events related to writing fan-fiction:</p>
<ul>
	<li>Short-stories</li>
	<li>Poetry</li>
	<li>Run-on stories on the message boards</li>
</ul>

<p><strong>Trivia</strong><br />
Your Star Wars and EH knowledge will be measured in these events, which can be administerd via e-mail, message board or IRC.</p>

<p><strong>Webpage Design and Creation</strong><br />
Events, competitions, units and other situations all require webpages for effective communication within the Fleet. Webpages creation is a good way to get noticed and promote your own fighting unit.</p>

<p><strong>Graphics and other Artistic Endeavors</strong><br />
Creating original EH-oriented graphics is a great way to contribute to the Fleet. It does not matter if you think you are the worst artist in the world, there is never enough graphic art in the EH.</p>

<p><strong>Newsletters</strong><br />
The Emperor's Hammer publishes the Dark Sentinel. This newsletter covers the entire EH and accepts contributions from all corners of the club. Give your work a broader audience. Be creative!</p>

<h4>Events</h4>
<p><strong>Operations and Competitions</strong><br />
All of the above activities can be done as an individual effort at any time. Don't wait for some "official" event to come along to be active! When official events do come along, it is always a big event. Official events fall into two categories:<br />
Operations and Competitions.</p>

<p>Operations are the Big Events. They are always Group-wide and frequently have many different activities to encourage everyone to participate. Because they are so complex, they involve many man-hours of preparation and do not occur more than once every few months.</p>

<p>Competitions are more local events. They are usually at the unit level or below, pitting squadron against squadron or platoon against platoon. Inter-squad competitions can settle who is the best flight in a squadron or squad in a platoon. Competitions may be run by anyone and only require approval by a superior and the Operation Officer for that Group.</p>
