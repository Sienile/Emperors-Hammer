<h2>Ranks</h2>

<p>A Member's overall status within the Fleet will be ENTIRELY determined by position. The following ranks are presented to further stratify the Emperor's Hammer and give something for members to strive to attain. The position of a member will ALWAYS take precedence over conflicting ranks (i.e. a Flight Leader who is a Lieutenant Colonel (LC) DOES NOT outrank a Squadron Commander who is a Captain...although this rarely occurs). The primary use of the ranks will be to determine minimum standards for the various Positions. </p>

<p>The ranks above High Admiral are reserved for Command Staff, but the ranks below it are used by TIE Corps (navy ranks). The Hammer's Fist utilizes a similar system (army ranks). The Directorate utilizes civilian nobility ranks and the Dark Brotherhood is unique.</p>

