<h2>Identification Lines</h2>
<p> Identification (ID) lines are used to display a member&rsquo;s position, rank, location within the fleet, medals and training. ID lines are used on all official EH emails and communiqu&#233;. This process ensures that members will know exactly with whom they are communicating. It is considered good email etiquette to only use one ID line in per email. This pertains to members belonging to more than one (1) Group.  The Command Staff and each Group have their own format for displaying ID lines.  Below you will find the Command Staff and TIE Corps ID lines explained. 
<a href="#GRP">Group specific ID line information</a> can be found in each Group Manual. </p>
            
<p><strong>How to display your ID line: </strong><br />
The general format for displaying an Emperor's Hammer or TIE Corps ID Line is:</p>

<p>Position&#47;Rank &#43; Name&#47;Position Designation&#47;Ship&#47;Medals highest to lowest &#91;FCHG&#93; &#123;IWATS&#125;</p>

<p>Example ID line:</p>
			
<p>FM&#47;LT Joe&#47;Alpha 3-4&#47;Wing I&#47;SSSD Sovereign GS&#47;ISM &#91;GALL&#93; &#123;IWATS&#125;</p>

<p>As you may have noticed, the actual ID line and the four tail sections are separated. The member identification and medal sections are separated by forward slashes ( <strong>&#47;</strong> ). The only use of brackets of any kind are permitted in the Fleet Commander's Honor Guard &#91;FCHG&#93; and Imperial Weapons and Tactics School &#123;IWATS&#125;, or training, sections.</p>
			
<p>Here is a more comprehensive example of a Command Staff member:</p>
            
<p>FC&#47;GA Cyric&#47;CS-1&#47;SSSD Sovereign
MoH&#47;IC&#47;GOE&#47;GSx2&#47;SSx7&#47;BSx2&#47;PCx2&#47;ISMx5&#47;IS-1BR&#47;MoI&#47;MoT-1rh-5gh&#47;LoC-CSx7-Rx1&#47;MoC-1doc-2boc&#47;CoLx3&#47;CoB&#47;LoAx3&#47;OV-11E
&#91;NONE&#93; &#123;IWATS-AIM-BX-CBX-FW-ICQ-IIC&#47;1-JS-M&#47;1&#47;2-SM&#47;1&#47;2&#47;3-TLN-WPN-XTM&#47;1-XTT&#125; </p>
            
<p>When ID lines start to grow in size like the above example it is common practice	to divide it into multiple lines:</p>

<p>FC&#47;GA Cyric&#47;CS-1&#47;SSSD Sovereign<br />
MoH&#47;IC&#47;GOE&#47;GSx2&#47;SSx7&#47;BSx2&#47;PCx2&#47;ISMx5&#47;IS-1BR&#47;MoI&#47;MoT-1rh-5gh&#47;LoC-CSx7-Rx1&#47;MoC-1doc-2boc&#47;CoLx3&#47;CoB&#47;LoAx3&#47;OV-11E<br />
&#91;NONE&#93; &#123;IWATS-AIM-BX-CBX-FW-ICQ-IIC&#47;1-JS-M&#47;1&#47;2-SM&#47;1&#47;2&#47;3-TLN-WPN-XTM&#47;1-XTT&#125;</p>
			  
