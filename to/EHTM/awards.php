			  <p>The system of awarding those upgrades is simple. Assume that the
			  members mentioned here are very active in terms of communication every
			  month. In the first two months of activity, members are rewarded with
			  the Bronze Oak Clusters. In the third month they are awarded the Silver
			  Oak Cluster instead of a Bronze Oak Cluster. In the fourth and fifth
			  month they are awarded yet again with Bronze Oak Clusters and in the
			  sixth month, they shall gain a Gold Oak Cluster. This process continues
			  until the reach Diamond Oak Cluster and then starts once again from the
			  beginning. Therefore, members active throughout all months in a year
			  should receive eight Bronze Oak Clusters and one of Silver, Gold,
			  Platinum and Diamond Oak Clusters. This is not a continuous process, so
			  if a member is somehow not active in a few months, he shall not be
			  penalized and when he returns he will continue his progress in this
			  &#x201c;queue&#x201d; as if nothing has happened.</p>
