<p><strong>Online Conferences/Meetings:</strong><br/>
Emperor's Hammer conferences are held on Internet Relay Chat (IRC) EVERY Saturday at 12 pm EST on the main channel <strong>#emperor's_hammer</strong>.&nbsp; These conferences are an excellent opportunity for the Fleet to get together, and to talk about the latest
events.&nbsp; Briefings are also held by Command Staff in attendance at each conference.</p>

<p><em>&quot;...as with ALL EH online meetings/conferences, attendance is voluntary, NOT mandatory.&nbsp; However, it is an amazing experience to talk to EH Members from all over the world at once.&nbsp; We have had Members from South Africa, Sweden, Canada, Hong Kong, England, and the U.S. all &quot;chatting&quot; at once.&nbsp; A LOT of fun and highly recommended by the Office of the Fleet Commander...!&quot; </em></p>

