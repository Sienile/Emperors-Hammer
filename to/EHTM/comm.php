<h2>Communications</h2>
<p>The Emperor's Hammer utilizes several forms of communication to make sure that all of its members are aware of the latest news and current activities.  The most common forms are:</p>

<h4>Email</h4>
<p>This form of communication is used the most. Email is used to send reports, make announcements and is the primary source of communication between members. Some groups and units have created mailing lists that you can sign up for to syat better informed of news and activities.</p>

<h4>Internet Relay Chat (IRC)</h4>
<p>mIRC is a chat program that the Emperor's Hammer uses to communicate with all of its members. Everyone from the very leaders in the Command Staff to the rank and file members use mIRC. mIRC is very much like MSN, AIM, or Yahoo, except you can chat with many different people all at once.<br />
The Emperor's Hammer encourages new members to use mIRC for many reasons:</p>
<ol>
<li>It's a great way to get to meet and converse with your peers and leaders and make new friends.</li>
<li>It's one of the best ways to get the latest news and information.</li>
<li>It's an excellent way to find activities and opportunities for getting promotions, awards, and recognition.</li>
<li>It's the best way to organize and setup multiplayer matches in games like X-Wing vs. TIE Fighter, X-Wing Alliance, Jedi Outcast, Jedi Academy, Battlefront I &amp; II, Empire at War, etc. with Emperor's Hammer and other club members.</li>
</ol>
<p>The basic IRC program can be downloaded from <a href="http://www.mirc.com/" target="blank"><strong>www.mirc.com</strong></a>. Other scripts for accessing IRC can be located via any search engine for those already familiar with IRC.  Many tutorials and help pages are available for the new user in many locations.  All EH channels are on the <strong>Undernet servers</strong>.<br />
Lists of active servers:</p>
<p><strong><a href="http://www.undernet.org/servers.php" target="blank">Undernet.org list</a></strong><br />
<strong><a href="http://mirchelp-undernet.org/servers.html" target="blank">#mIRCHelp! list</a></strong></p>
