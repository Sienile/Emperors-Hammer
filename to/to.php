<h2>Who is the Emperor's Hammer Training Officer?</h2>

<h3>Training Officer (TO)</h3>
<p>Position Description: The Training Officer oversees all aspects of training within the Emperor's Hammer. This spans from the initial instruction of new recruits to the running of Imperial University. In addition, the Training Officer maintains the EH Training Manual. The Training Officer is also available to assist all members, new and old, with all aspects of the Emperor's Hammer.</p>

<p>Rank: The Training Officer is Fifth in Command of the Fleet and must hold the Rank of Admiral or higher. The Training Officer can be promoted up to and including the rank of High Admiral.</p>

<p>Duties:</p>
<ul>
 <li>Overseeing the operation of Imperial University (IU)</li>
 <li>Operation of courses and exams</li>
 <li>Running Training Office competitions</li>
 <li>Updating the Training Manual</li>
 <li>Assist all members in training matters</li>
 <li>Overseeing the Group Training Officers and Group Academies</li>
</ul>
<hr />
<p>The Training Office is now the entry point for every recruit of the Emperor's Hammer. No matter what Group performed the recruiting, new recruits must pass through this office before being sent to a group for assignment.</p>
<p>Another significant change is the new positioning of the Training Officer. The Group Training Officers now respond not only to the Group Commanding Officers, but to the TO. This will ensure that training procedures and content will have a consistent standard of excellence spanning the entire Emperor's Hammer.</p>
<p>With the introduction of the Imperial University, generic non-group based instruction such as basic training, programming, leadership/unit management and cultures are removed from the Group Academies where they were mixed with group-themed courses, and are now available to the entire fleet in Imperial University.</p>

