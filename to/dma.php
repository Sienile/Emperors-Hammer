<p align="justify">The Imperial Weapons and Tactics School IWATS has become the premiere starfighter pilot training
institution for the Emperor's Hammer Strike Fleet. The IWATS 'classes' are held on
Sif in the Phare System and conduct starfighter training exercises near Baldyr, a moon of
Ullyr (see the Systems Manual for a description of the <i>Phare System</i>). There are
already a dozen &quot;courses&quot; available for EH Members to take including Fighter
Identification, XWing vs TIE Fighter tactics, EH Mission design, and HTML/PHP/ASP website
editing/creation...Several &quot;Majors&quot; are also available. The courses
included in the IWATS training program are highly recommended for <u>all</u> EH Members.
&nbsp; Also note, that successful completion of the basic IWATS training is required to be
accepted into the Aggressor Wing.</p>

