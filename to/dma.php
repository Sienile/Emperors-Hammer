<h2 style="text-align:center">Training at the Imperial University on Aurora</h2>

<p style="text-align:center"><a href="http://www.emperorshammer.org/acadabout.php?id=1" target="_blank"><img alt="Imperial University" title="Visit the Imperial University" border="0" src="images/imperialuNEW.JPG" width="70%" /></a></p>

<p>The Imperial University located on the Aurora is the training center for the Emperor's Hammer. This training center has grown to encompass training for all Emperor's Hammer members and group specific members alike. Here you will find all of the courses you will need to improve your skills or to fulfill any requirements for advancement. Basic Training, History, Cultures, Communications, Programming/Web Development and Command Level courses for all members are available here.</p>

<p>Imperial University hosts many courses and contains seperate academies that provide instruction on gaming, technical and general courses and certifications that apply to all Emperor's Hammer members.  Group themed courses can be found in the respective academies listed below.</p>

